package LineExpressions1;

public class lineinieVirazhenia1 {

	public static void main(String[] args) {
		int x = 2;
		int y = 4;
		int s;
		int r;
		int pr;
		double ch;
		s = x + y;
		r = x - y;
		pr = x *y;
		ch = x%y;
		System.out.println ("symma " +s);
		System.out.println ("raznost " +r);
		System.out.println ("proizvedenie " +pr);
		System.out.println ("chasnoe " + ch);
	}

}
